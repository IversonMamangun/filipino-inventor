<script>
 function toggleDetails(event) {
  const details = [
    document.getElementById("detail_1"),
    document.getElementById("detail_2"),
    document.getElementById("detail_3"),
    document.getElementById("detail_4")
  ];

  const button = event.target;

  // Toggle each detail
  details.forEach(detail => {
    const isHidden = detail.classList.toggle("h-13");
    detail.classList.toggle("overflow-hidden", isHidden);
    button.textContent = isHidden ? "View" : "Hide";
  });
}


</script>